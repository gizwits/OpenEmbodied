import wave
import struct

def hex_pcm_to_wav(hex_data, output_file, channels=1, sample_width=2, sample_rate=16000):
    # 移除空格和换行符，每两个字符分割成一组
    hex_pairs = ''.join(hex_data.split()).replace('0x', '')
    hex_pairs = [hex_pairs[i:i+2] for i in range(0, len(hex_pairs), 2)]
    
    # 将16进制对转换为有符号16位整数
    samples = []
    for i in range(0, len(hex_pairs), 2):
        if i + 1 < len(hex_pairs):
            # 小端序，低字节在前
            value = int(hex_pairs[i+1] + hex_pairs[i], 16)
            # 转换为有符号整数
            if value > 32767:
                value -= 65536
            samples.append(value)

    # 创建WAV文件
    with wave.open(output_file, 'w') as wav_file:
        wav_file.setnchannels(1)
        wav_file.setsampwidth(sample_width)
        wav_file.setframerate(sample_rate)
        
        # 将样本写入WAV文件
        for sample in samples:
            wav_file.writeframes(struct.pack('<h', sample))

